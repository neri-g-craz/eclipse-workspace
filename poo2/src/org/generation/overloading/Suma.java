package org.generation.overloading;
import java.math.BigDecimal;
public class Suma {
	//Crear dos metodos llamados sumar, uno que reciba dos parámetros de tipo int y el otro reciba dos parámetros de tipo double
	
	static int sumar (int num1, int num2) {
		return num1 + num2;
	}
	static double sumar (double num1, double num2) {
		return num1 + num2;
	}
	
}
